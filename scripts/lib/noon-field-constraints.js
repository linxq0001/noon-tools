export function normalizeNoonSelectValue(label, value) {
  const raw = String(value || "").trim();
  const alias = selectAliases.get(`${label}:${raw.toLowerCase()}`);
  if (alias) return alias;

  const allowed = noonSelectConstraints[label];
  if (!allowed) return raw;

  return allowed.find((option) => option.toLowerCase() === raw.toLowerCase()) ?? raw;
}

export function constrainNoonSelectValue(label, value, fallback = "") {
  const normalized = normalizeNoonSelectValue(label, value);
  const allowed = noonSelectConstraints[label];
  if (!allowed) return normalized;
  if (allowed.includes(normalized)) return normalized;
  return fallback && allowed.includes(fallback) ? fallback : allowed[0] ?? normalized;
}
